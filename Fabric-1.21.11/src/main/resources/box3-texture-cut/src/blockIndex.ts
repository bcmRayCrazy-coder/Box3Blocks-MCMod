export type FaceName = "left" | "right" | "bottom" | "top" | "front" | "back";

export function getSoildBlockTexturePositionById(blockId: number): {
  left: Array<{ x: number; y: number }>;
  right: Array<{ x: number; y: number }>;
  bottom: Array<{ x: number; y: number }>;
  top: Array<{ x: number; y: number }>;
  front: Array<{ x: number; y: number }>;
  back: Array<{ x: number; y: number }>;
} {
  const id = blockId & 4095;
  const index = BlockIndex.ids.findIndex((e) => e === id);
  if (index === -1) {
    throw new Error(`Error: Block ${blockId} doesn't exist!`);
  }
  if (isFluid(blockId)) {
    throw new Error(`Error: Block ${blockId} isn't fluid!`);
  }
  const basePtr = index * 6;
  console.log(basePtr);
  const faceNameTable = {
    0: "left",
    1: "right",
    2: "bottom",
    3: "top",
    4: "front",
    5: "back",
  };
  const faces: {
    left: Array<{ x: number; y: number }>;
    right: Array<{ x: number; y: number }>;
    bottom: Array<{ x: number; y: number }>;
    top: Array<{ x: number; y: number }>;
    front: Array<{ x: number; y: number }>;
    back: Array<{ x: number; y: number }>;
  } = {
    left: [],
    right: [],
    bottom: [],
    top: [],
    front: [],
    back: [],
  };
  for (let face = 0; face < 6; face++) {
    const faceName = faceNameTable[face as 0 | 1 | 2 | 3 | 4 | 5];
    const facePtr = basePtr + face;
    // console.warn(facePtr);
    for (let frame = 0; frame < BlockIndex.animLength[facePtr]; frame++) {
      const position = _getFramePos(BlockIndex.texture[facePtr], frame);
      faces[faceName as FaceName].push(position);
    }
  }
  return faces;
}

